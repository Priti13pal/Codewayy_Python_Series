# Tuple Method in Python:

# Tuple is a collection which is ordered and unchangeable. Allows duplicate members.

Tuple = ("Computer technology", "Information technology", "Computer Science")
print(Tuple)

# Show the length of Tuple
print(len(Tuple))

# Returns the number of times a specified value occurs in a tuple
print(Tuple.count("Information technology"))

# Searches the tuple for a specified value and returns the position of where it was found
print(Tuple.index("Computer Science"))

# Maximum number or string in the tuple
print(max(Tuple))

# Minimum number or string in the tuple
print(min(Tuple))


print(Tuple[-1])


print(Tuple[0:2])







